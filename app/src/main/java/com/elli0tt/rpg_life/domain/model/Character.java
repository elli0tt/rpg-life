package com.elli0tt.rpg_life.domain.model;

public class Character {
    private String name;
    private String heroClass;
    private int level;
    private int totalXp;

    public String getName() {
        return name;
    }

    public String getHeroClass() {
        return heroClass;
    }

    public int getLevel() {
        return level;
    }

    public int getTotalXp() {
        return totalXp;
    }
}
